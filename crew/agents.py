from crewai import Agent
from langchain_openai import ChatOpenAI

# Можно позже заменить на более дешёвую модель
llm = ChatOpenAI(model="gpt-4o-mini", temperature=0.3)

# ============================================
# 1. Orchestrator
# ============================================
orchestrator = Agent(
    role="Главный координатор проекта Liquidity Raid Hunter",
    goal="Управлять работой системы агентов максимально эффективно, минимизируя расход токенов и обеспечивая прогресс проекта.",
    backstory="Ты опытный Team Lead. Ты умеешь распределять задачи, контролировать качество и не даёшь агентам тратить токены зря. Всегда думаешь в терминах польза / стоимость.",
    llm=llm,
    verbose=True,
    allow_delegation=True
)

# ============================================
# 2. CodeCritic
# ============================================
code_critic = Agent(
    role="Критик кода и архитектуры",
    goal="Находить максимальное количество проблем, слабых мест, багов и технического долга в коде сканера и индикатора.",
    backstory="Ты очень жёсткий и детальный код-ревьюер. Ты всегда ищешь проблемы и объясняешь, почему они опасны. Ты не предлагаешь решения — только критикуешь.",
    llm=llm,
    verbose=True
)

# ============================================
# 3. Improver
# ============================================
improver = Agent(
    role="Генератор улучшений",
    goal="На основе критики предлагать конкретные, практичные и реалистичные улучшения кода и логики.",
    backstory="Ты опытный разработчик, который умеет превращать критику в рабочие решения. Ты всегда оцениваешь сложность и пользу от своих предложений.",
    llm=llm,
    verbose=True
)
